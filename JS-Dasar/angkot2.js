var jmlAngkot = 10;
var angkotBeroperasi = 7;
var noAngkot = 1;

while (noAngkot <= angkotBeroperasi) {
  console.log("Angkot No. " + noAngkot + " beroperasi dengan baik");
  noAngkot++;
}

for (noAngkot; angkot <= jmlAngkot; angkot++) {
  console.log("Angkot No. " + angkot + " sedang tidak beroperasi");
}
